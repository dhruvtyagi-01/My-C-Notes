#include <stdio.h>

int main()
{
    int a = 10;
    int b = 5;
    int c = a + b;
    int d = a - b;
    int e = a * b;
    int f = a % b;
    printf("The sum is %d\n", c);
    printf("The difference is %d\n", d);
    printf("The product is %d\n", e);
    printf("The remainder is %d", f);

    return 0;
}