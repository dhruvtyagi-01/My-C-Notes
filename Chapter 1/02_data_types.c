#include <stdio.h>

int main()
{
    int a = 1;      // integer datatype
    float b = 1.6;  // float datatype
    char c = 'cat'; // character datatype

    printf("The value of \'a\' is %d\n", a);
    printf("The value of \'b\' is %f\n", b);
    printf("The value of \'c\' is %c\n", c);

    return 0;
}